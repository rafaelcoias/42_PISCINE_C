int	ft_strcmp(char *s1, char *s2)
{
	int		pos;

	pos = 0;
	while (s1[pos] != '\0' && s2[pos] != '\0' && s1[pos] == s2[pos])
		pos++;
	return (s1[pos] - s2[pos]);
}
